# Define a variável INDICE com o valor 13
INDICE = 13
# Define a variável SOMA com o valor 0, que vai acumular a soma dos números
SOMA = 0
# Define a variável K com o valor 0 que vai ser usada para contar de 1 até INDICE
K = 0

# Enquanto K for menor que INDICE (13) vai ser executado
while K < INDICE:
    # Adiciona 1 ao valor de K a cada repetição do loop
    K = K + 1
    # Adiciona o valor de K à variável SOMA
    SOMA = SOMA + K

# Imprime o valor total de SOMA
print(SOMA)